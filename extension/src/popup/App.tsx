import { useEffect, useState } from 'react';
import { useAuth } from './hooks/useAuth';
import Login from './pages/Login';
import Signup from './pages/Signup';
import SetUsername from './pages/SetUsername';
import Dashboard from './pages/Dashboard';
import Profile from './pages/Profile';
import Friends from './pages/Friends';
import Leaderboard from './pages/Leaderboard';
import Settings from './pages/Settings';
import BottomNav from './components/BottomNav';
import { usePendingFriendRequestsCount } from './hooks/usePendingFriendRequestsCount';

function getProfileInitials(displayName: string | null | undefined, username: string): string {
  const source = (displayName?.trim() || username).replace(/^@+/, '').trim();
  if (!source) return 'U';

  const words = source
    .split(/[\s._-]+/)
    .map((part) => part.trim())
    .filter(Boolean);

  if (words.length >= 2) {
    return `${words[0][0]}${words[1][0]}`.toUpperCase();
  }

  const compact = words[0] ?? source;
  return compact.slice(0, 2).toUpperCase();
}

function App() {
  const {
    user,
    profile,
    loading,
    profileError,
    signIn,
    signUp,
    signOut,
    updateUsername,
    refreshProfile,
  } = useAuth();
  const [authView, setAuthView] = useState<'login' | 'signup'>('login');
  const [activePage, setActivePage] = useState('home');
  const [viewingProfileId, setViewingProfileId] = useState<string | null>(null);
  const { count: pendingRequestsCount, refresh: refreshPendingRequests } = usePendingFriendRequestsCount(user?.id ?? null);
  const [pendingRequestsLocal, setPendingRequestsLocal] = useState(0);

  useEffect(() => {
    setPendingRequestsLocal(pendingRequestsCount);
  }, [pendingRequestsCount]);

  useEffect(() => {
    // Prevent stale page state (for example, staying on Settings) across logout/login user changes.
    setActivePage('home');
    setViewingProfileId(null);
    if (!user) setAuthView('login');
  }, [user?.id]);

  if (loading) {
    return (
      <div className="w-[400px] h-[600px] bg-doom-bg flex items-center justify-center">
        <div className="text-center">
          <p className="text-4xl mb-3 animate-pulse-neon">ðŸ’€</p>
          <p className="text-doom-muted font-mono text-xs">Loading...</p>
        </div>
      </div>
    );
  }

  // Not authenticated
  if (!user) {
    return (
      <div className="w-[400px] h-[600px] bg-doom-bg text-white">
        {authView === 'login' ? (
          <Login
            onSignIn={async (email, password) => {
              await signIn(email, password);
            }}
            onSwitchToSignUp={() => setAuthView('signup')}
          />
        ) : (
          <Signup
            onSignUp={async (email, password, displayName) => {
              await signUp(email, password, displayName);
            }}
            onSwitchToLogin={() => setAuthView('login')}
          />
        )}
      </div>
    );
  }

  const currentUser = user;
  const currentProfile = profile;
  const fallbackUsername = currentUser.email?.split('@')[0]?.toLowerCase().replace(/[^a-z0-9_]/g, '_') || 'user';
  const headerUsername = currentProfile?.username ?? fallbackUsername;
  const headerInitials = getProfileInitials(currentProfile?.display_name, headerUsername);
  const profileReady = Boolean(currentProfile);

  // Authenticated but needs to set username
  const isAutoGenerated = currentProfile
    ? /^[a-z0-9]+_[a-f0-9]{4}$/.test(currentProfile.username)
    : false;
  if (currentProfile && isAutoGenerated) {
    return (
      <div className="w-[400px] h-[600px] bg-doom-bg text-white">
        <SetUsername
          currentUsername={currentProfile.username}
          onSetUsername={updateUsername}
        />
      </div>
    );
  }

  // Render the active page
  function renderPage() {
    switch (activePage) {
      case 'home':
        return <Dashboard />;
      case 'board':
        return (
          <Leaderboard
            userId={currentUser.id}
            onViewProfile={(id) => {
              setViewingProfileId(id);
              setActivePage('profile');
            }}
          />
        );
      case 'pals':
        return (
          <Friends
            userId={currentUser.id}
            onPendingRequestsChanged={(count) => {
              setPendingRequestsLocal(count);
              void refreshPendingRequests();
            }}
            onViewProfile={(id) => {
              setViewingProfileId(id);
              setActivePage('profile');
            }}
          />
        );
      case 'battle':
        return <div className="text-center text-doom-muted py-8 font-mono text-sm">Battles coming in v3...</div>;
      case 'chat':
        return <div className="text-center text-doom-muted py-8 font-mono text-sm">AI Chat coming in v2...</div>;
      case 'profile':
        return (
          <Profile
            userId={viewingProfileId ?? currentUser.id}
            isOwnProfile={!viewingProfileId || viewingProfileId === currentUser.id}
            onBack={viewingProfileId ? () => setActivePage('home') : undefined}
            cachedProfile={(!viewingProfileId || viewingProfileId === currentUser.id) ? currentProfile ?? undefined : undefined}
          />
        );
      case 'settings':
        if (!currentProfile) {
          return (
            <div className="card text-center py-6">
              <p className="text-doom-muted text-sm font-mono mb-3">
                {profileError ?? 'Syncing profile...'}
              </p>
              <div className="flex items-center justify-center gap-2">
                <button
                  onClick={() => {
                    void refreshProfile();
                  }}
                  className="px-3 py-1.5 rounded-md border border-doom-border text-xs font-mono text-doom-muted hover:text-white hover:border-neon-green/40 transition-colors"
                >
                  Retry
                </button>
                <button
                  onClick={() => {
                    void signOut();
                  }}
                  className="px-3 py-1.5 rounded-md border border-doom-border text-xs font-mono text-doom-muted hover:text-neon-pink transition-colors"
                >
                  Sign out
                </button>
              </div>
            </div>
          );
        }
        return (
          <Settings
            profile={currentProfile}
            onSignOut={signOut}
            onProfileUpdated={() => {
              // Profile will auto-refresh via useAuth listener
            }}
          />
        );
      default:
        return <Dashboard />;
    }
  }

  // Fully authenticated
  return (
    <div className="w-[400px] h-[600px] bg-doom-bg text-white flex flex-col">
      <header className="flex items-center justify-between px-4 py-3 border-b border-doom-border">
        <h1 className="text-lg font-bold font-mono neon-text-green">
          DoomScroller
        </h1>
        <div className="flex items-center gap-2">
          <button
            onClick={() => {
              setViewingProfileId(null);
              setActivePage('profile');
            }}
            className="w-9 h-9 rounded-full bg-doom-surface border border-doom-border text-white text-xs font-semibold tracking-wide hover:border-neon-green/40 hover:shadow-neon-green transition-all"
            title={`@${headerUsername}`}
            aria-label={`Open profile for @${headerUsername}`}
          >
            {headerInitials}
          </button>
          <button
            onClick={() => setActivePage('settings')}
            className="w-9 h-9 rounded-full bg-doom-surface border border-doom-border text-doom-muted hover:text-white hover:border-neon-green/40 transition-all text-lg leading-none flex items-center justify-center"
            title="Settings"
            aria-label="Open settings"
          >
            âš™
          </button>
        </div>
      </header>

      {!profileReady && (
        <div className="mx-4 mt-3 p-3 rounded-lg border border-doom-border bg-doom-surface/60">
          <p className="text-doom-muted text-xs font-mono text-center">
            {profileError ?? 'Syncing profile...'}
          </p>
          <div className="flex items-center justify-center gap-2 mt-2">
            <button
              onClick={() => {
                void refreshProfile();
              }}
              className="px-3 py-1.5 rounded-md border border-doom-border text-xs font-mono text-doom-muted hover:text-white hover:border-neon-green/40 transition-colors"
            >
              Retry
            </button>
            <button
              onClick={() => {
                void signOut();
              }}
              className="px-3 py-1.5 rounded-md border border-doom-border text-xs font-mono text-doom-muted hover:text-neon-pink transition-colors"
            >
              Sign out
            </button>
          </div>
        </div>
      )}

      <main className="flex-1 overflow-y-auto p-4">
        {renderPage()}
      </main>

      <BottomNav
        active={activePage}
        onNavigate={setActivePage}
        palsPendingCount={pendingRequestsLocal}
      />
    </div>
  );
}

export default App;
